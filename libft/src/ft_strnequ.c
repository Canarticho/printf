#include "libft.h"

int	ft_strnequ(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	if (s1 && s2)
	{
		i = 0;
		while (s1[i] == s2[i] && i < n && s1[i] && s2[i])
		{
			i++;
		}
		if ((i == n || s1[i] == s2[i]))
			return (1);
	}
	return (0);
}
