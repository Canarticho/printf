#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int end;
	int i;

	i = -1;
	end = ft_strlen(s1);
	while (s2[++i] && n--)
		s1[end + i] = (char)s2[i];
	s1[end + i] = '\0';
	return (s1);
}
